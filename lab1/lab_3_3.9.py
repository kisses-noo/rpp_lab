# Задание 3.9. Работа со списками. Операции над списками в Python.

import random

# Заполняем массив A случайными элементами
A = []
for i in range(10):
    A.append(random.randint(-100, 100))
print("Массив A:", A)

# Находим минимальный по модулю элемент
min_abs = min(abs(x) for x in A)
print("Минимальный по модулю элемент:", min_abs)

# Выводим массив в обратном порядке
A_reversed = A[::-1]
print("Массив A в обратном порядке:", A_reversed)

# Заполняем массив B случайными элементами
B = []
for i in range(10):
    B.append(random.randint(-100, 100))
print("Массив B:", B)

# Меняем местами элементы массивов A и B
A, B = B, A

# Вывод массивов A и B после обмена
print("После обмена:")
print("Массив A:", A)
print("Массив B:", B)